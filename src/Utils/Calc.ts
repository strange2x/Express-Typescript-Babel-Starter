export const addition = (numberOne: number, numberTwo: number): number =>
  numberOne + numberTwo;

export const subtraction = (numberOne: number, numberTwo: number): number =>
  numberOne - numberTwo;

export const multiplicaton = (numberOne: number, numberTwo: number): number =>
  numberOne * numberTwo;

export const division = (numberOne: number, numberTwo: number): number =>
  numberOne / numberTwo;
